// Big O Guide
// Big O is a measure of how the runtime of an algorithm grows as the input size grows
// when the calculation is not dependent on the input size, the time complexity is O(1) - constant time complexity
// loop - O(n) - linear time complexity
// nested loop - O(n^2) - quadratic time complexity
// When input size is reduced by half, the time complexity is O(log n) - logarithmic time complexity