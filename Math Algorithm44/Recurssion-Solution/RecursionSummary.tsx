// - Recursion is a problem solving technique where a solution depends on 
// solutions to smaller instances of the same problem
// Recursion is when a function calls itself

// WHY DO WE NEED RECURSION

// - Its a great technique to simplify your solution
// - If you find yourself breaking down a problem into smaller versions of the same problem
// recursion is very useful


// KEY POINTS IN RECURSION
// 1. Every recursion solution needs to have a base case i.e a condition to terminate the recursion
// If you don't have a base case, you will have an infinite loop which will crush the program
// 2. Recursion might simply solving the problem but it does not always translate to a faster solution
// A recursive solution might be far worse compared to an iterative solution


// TIPS FOR RECURSIVE SOLUTIONS
// - Figure out how to break down the problem into smaller versions of the same problem
// -identify the base case for recursion